package Chap02;

public class InputOuput01 {

	public static void main(String[] args) {
		
		String myName = "홍길동"; //문자열 myName 선언
		int myAge = 20; // 정수형 myAge 선언
		
		System.out.println("이름 : " + myName); // 문자열 "이름 : "을 출력하고 +를 사용해 뒤에 변수 myName도 같이 출력
		System.out.println("나이 : " + myAge); // 문자열 "나이 : "을 출력하고 +를 사용해 뒤에 변수 myAge도 같이 출력
		

	}

}
