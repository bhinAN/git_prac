package Chap02;

public class Datatype03 {

	public static void main(String[] args) {
		char ch = 'J'; /*
		ch는 문자형으로 문자와 숫자 등 문자 집합의 기호를 나타내는 데 사용함
		작은 따옴표로 값을 묶어 표현함
		*/
		char ch2 = 'a'; 
		char ch3 = 'v';
		char ch4 = 'a';
		System.out.println(ch); //ch 변수 출력
		System.out.println(ch2); //ch2변수 출력
		System.out.println(ch3); //ch3변수 출력
		System.out.println(ch4); //ch4변수 출력
	}

}
