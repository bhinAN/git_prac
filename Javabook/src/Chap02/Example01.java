package Chap02;

public class Example01 {
	
	public static void main(String[] args) {
		
		int SPEED = 20; // 정수형 변수 SPEED 선언
		int count = 10; // 정수형 변수 count 선언
 		SPEED = 40; // SPEED 변수에 정수 40을 집어넣음
		count = 11; // count 변수에 정수 40을 집어넣음
		
		System.out.print("변수 SPEED 값은"); // 문자열 "변수 SPEED 값은" 을 출력
		System.out.println(SPEED); // 변수 SPEED 출력
		System.out.print("변수 count 값은 "); // 문자열 "변수 count 값은" 을 출력
		System.out.println(count); // 변수 count 출력
	}

}
